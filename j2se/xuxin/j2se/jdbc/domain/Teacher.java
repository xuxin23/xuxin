package xuxin.j2se.jdbc.domain;

/**
 * 教师域模型
 * @version 1.0
 * @author xuxin 
 * @data 2012-12-22
 */
public class Teacher {

    private String teacherId;
    private String teacherName;
    private String teacherSex;
    private String teacherAddress;
    private String teacherAge;
    private String teacherLevel;
    private String teacherOthers;
    private String teacherMore;
    public String getTeacherId() {
        return teacherId;
    }
    public void setTeacherId(String teacherId) {
        this.teacherId = teacherId;
    }
    public String getTeacherName() {
        return teacherName;
    }
    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }
    public String getTeacherSex() {
        return teacherSex;
    }
    public void setTeacherSex(String teacherSex) {
        this.teacherSex = teacherSex;
    }
    public String getTeacherAddress() {
        return teacherAddress;
    }
    public void setTeacherAddress(String teacherAddress) {
        this.teacherAddress = teacherAddress;
    }
    public String getTeacherAge() {
        return teacherAge;
    }
    public void setTeacherAge(String teacherAge) {
        this.teacherAge = teacherAge;
    }
    public String getTeacherLevel() {
        return teacherLevel;
    }
    public void setTeacherLevel(String teacherLevel) {
        this.teacherLevel = teacherLevel;
    }
    public String getTeacherOthers() {
        return teacherOthers;
    }
    public void setTeacherOthers(String teacherOthers) {
        this.teacherOthers = teacherOthers;
    }
    public String getTeacherMore() {
        return teacherMore;
    }
    public void setTeacherMore(String teacherMore) {
        this.teacherMore = teacherMore;
    }
    
    
}
