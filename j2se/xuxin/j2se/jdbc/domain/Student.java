package xuxin.j2se.jdbc.domain;

/**
 * 学生域模型
 * @version 1.0
 * @author xuxin 
 * @data 2012-12-22
 */
public class Student {

    private String studentId;
    private String StudentName;
    private String studentAge;
    private String studentSex;
    private String studentPhoneNo;
    private String studentAddress;
    private String studentPoint;
    private String studentOthers;
    private String studentMore;
    
    public String getStudentId() {
        return studentId;
    }
    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }
    public String getStudentName() {
        return StudentName;
    }
    public void setStudentName(String studentName) {
        StudentName = studentName;
    }
    public String getStudentAge() {
        return studentAge;
    }
    public void setStudentAge(String studentAge) {
        this.studentAge = studentAge;
    }
    public String getStudentSex() {
        return studentSex;
    }
    public void setStudentSex(String studentSex) {
        this.studentSex = studentSex;
    }
    public String getStudentPhoneNo() {
        return studentPhoneNo;
    }
    public void setStudentPhoneNo(String studentPhoneNo) {
        this.studentPhoneNo = studentPhoneNo;
    }
    public String getStudentAddress() {
        return studentAddress;
    }
    public void setStudentAddress(String studentAddress) {
        this.studentAddress = studentAddress;
    }
    public String getStudentPoint() {
        return studentPoint;
    }
    public void setStudentPoint(String studentPoint) {
        this.studentPoint = studentPoint;
    }
    public String getStudentOthers() {
        return studentOthers;
    }
    public void setStudentOthers(String studentOthers) {
        this.studentOthers = studentOthers;
    }
    public String getStudentMore() {
        return studentMore;
    }
    public void setStudentMore(String studentMore) {
        this.studentMore = studentMore;
    }
    
    
}
